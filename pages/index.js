import Head from "next/head";
import { useEffect, useState } from "react";
import styles from "../styles/Home.module.css";
import {
  Button,
  Table,
  Tbody,
  Td,
  Th,
  Thead,
  Tr,
  useDisclosure,
} from "@chakra-ui/react";
import NewsModal from "../components/NewsModal/NewsModal";
import { axiosGet } from "../helpers/axiosCalls";
import { format } from "date-fns";
import { EditIcon } from "@chakra-ui/icons";
import DeleteNews from "../components/DeleteNews/DeleteNews";
import CreateNewsForm from "../components/CreateNewsForm/CreateNewsForm";

export default function Home() {
  const [news, setNews] = useState([]);
  const [tags, setTags] = useState([]);
  const { isOpen, onOpen, onClose } = useDisclosure();
  useEffect(() => {
    axiosGet("/news").then((res) => setNews(res.data.data));
    axiosGet("/tags").then((res) => setTags(res.data.data));
  }, []);

  return (
    <div className={styles.container}>
      <NewsModal
        tags={tags}
        setNews={setNews}
        onClose={onClose}
        onOpen={onOpen}
      />

      <Head>
        <title>News Api</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Table variant="simple">
        <Thead>
          <Tr>
            <Th>ID</Th>
            <Th>Headline</Th>
            <Th>Date</Th>
            <Th isNumeric></Th>
          </Tr>
        </Thead>
        <Tbody>
          {news
            .sort(function (a, b) {
              return a.id - b.id;
            })
            .map((news) => {
              return (
                <Tr key={news.id}>
                  <Td>{news.id}</Td>
                  <Td>{news.headline}</Td>
                  <Td>{format(new Date(news.date), "dd-MM-yyyy")}</Td>
                  <Td isNumeric>
                    <NewsModal
                      id={news.id}
                      setNews={setNews}
                      onClose={onClose}
                      onOpen={onOpen}
                      create={false}
                    />
                    <DeleteNews id={news.id} setNews={setNews} />
                  </Td>
                </Tr>
              );
            })}
        </Tbody>
      </Table>
    </div>
  );
}
